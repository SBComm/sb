                        <p><strong>Parking for commuters</strong> is abundant and easily accessible, with free bus service to all buildings on campus. Parking is available in the parking garages on East and West Campus and in metered parking areas, as shown on the <a href="downloads/pdf/14061566-Stony-Brook-Campus-Map.pdf" target="_blank">campus map</a>. See the <a class="external-link" href="http://www.stonybrook.edu/parking" target="_blank">parking page for more information.</a></p>
                        <p><strong>Motorist Assistance.</strong> If you experience a problem with your car, help is just a phone call away. Staff are equipped to jump-start cars, help if you have locked your keys in your car, assist with slow-leaking tires, and get you going if you run out of gas. If you have additional questions, please call the Parking Services Office at (631) 632-AUTO. (Monday through Friday from 8:00 a.m. to 3:00 p.m.)</p>
                        <p>The <strong><a class="external-link" href="http://studentaffairs.stonybrook.edu/css/" target="_blank">Office of Commuter Student Services</a></strong> is located in room 225 in the Student Activities Center.</p>
