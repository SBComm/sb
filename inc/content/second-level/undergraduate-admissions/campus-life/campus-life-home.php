                        <p><strong><a class="external-link" href="http://och.fsa.sunysb.edu/" target="_blank">Off-Campus Housing Service (OCH)</a></strong> provides a current listing of available local housing. If you need assistance finding housing based on individual requirements, our friendly staff at OCH will provide a personalized search to assist you.</p>
