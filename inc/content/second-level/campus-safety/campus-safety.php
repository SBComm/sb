                    <h3>Traffic &amp; Pedestrian Safety</h3>
                    <div class="content col-two-third">
                        <h4>Don’t Text and Drive!</h4>
                        <p><strong>FACT:</strong> Drivers who use their phones behind the wheel are 23 times more likely to crash.</p>
                        <p>Drivers on campus can take a few simple steps to prevent accidents:</p>
                        <ul class="default-bullets">
                            <li>Silence your phone and put it out of reach when driving.</li>
                            <li>Download a free app that will silence message alerts and send automatic replies that you’re driving.</li>
                            <li>Pull over and park if you must use your phone.</li>
                        </ul>
                        <p><a class="external-link" href="http://www.asa.stonybrook.edu/police/webrequests/DistractionsPledge.aspx" target="_blank">Make the pledge to stop distractions.</a></p>
                        
                        <h4>Distracted? Impacted!</h4>
                        <p>Pedestrians, pay attention and protect yourself when walking on campus:</p>
                        <ul class="default-bullets">
                            <li>Don’t text or talk on your phone while walking.</li>
                            <li>Cross at the crosswalk.</li>
                            <li>Use sidewalks when available.</li>
                            <li>Be Safe, Be Seen: Wear reflective gear at night.</li>
                        </ul>

                    </div>
