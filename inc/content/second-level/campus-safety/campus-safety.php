                        <h4>Red Watch Band</h4>
                        <p>Toxic drinking is an epidemic on campuses all across America. It means consuming so much alcohol the drinker passes out. But while "sleeping it off," the victim may be quietly dying.</p>
                        <p>The <a class="external-link" href="http://www.stonybrook.edu/sb/redwatchband/" title="Red Watch Band" target="_blank">Red Watch Band</a> movement is designed to end alcohol overdose deaths by teaching students how to handle alcohol emergencies and summon professional help.</p>
