                    <h3>Alcohol &amp; Drugs</h3>
                    <div class="content col-two-third">
                        <h4>Center for Prevention and Outreach (CPO)</h4>
                        <p><a class="external-link" href="http://studentaffairs.stonybrook.edu/cpo/index.shtml" title="Center for Prevention &amp; Outreach" target="_blank">CPO</a> offers a wide range of services to address student concerns about alcohol and other substances.</p>
